public class TypeUtils {
    /**
     * 
     搞笑牛人=1
     网络短剧=2
     搞笑游戏=3
     搞笑动画=4
     相声小品=5
     综艺=6
     配音=7
     直播=8
     集锦=9
     */
    public static String getType(String mclass)
    {
        if (mclass.equals("1"))
        {
           return "搞笑牛人";
        }else if (mclass.equals("2"))
        {
            return "搞笑牛人";
        }
        else if (mclass.equals("3"))
        {
            return "搞笑牛人";
        }
        else if (mclass.equals("4"))
        {
            return "搞笑牛人";
        }
        else if (mclass.equals("5"))
        {
            return "搞笑牛人";
        }
        else if (mclass.equals("6"))
        {
            return "搞笑牛人";
        }
        else if (mclass.equals("7"))
        {
            return "搞笑牛人";
        }
        else if (mclass.equals("8"))
        {
            return "搞笑牛人";
        }
        else if (mclass.equals("9"))
        {
            return "搞笑牛人";
        }
        return "";

    }
    
    
}
